def gmul(a, b):
    p = 0
    for _ in range(8):
        if b & 1: p ^= a
        hi = a & 0x80
        a = (a << 1) & 0xff
        if hi: a ^= 0x1b
        b >>= 1
    return p

# T0[a] = (gmul(2, S(a)), S(a), S(a), gmul(3, S(a))) packed as 32-bit
# AES MixColumns matrix column 0 is (02, 01, 01, 03).
# T1, T2, T3 are rotations of T0 by 8 bits.
def make_T(col_idx):
    """T_col_idx[a] is a 32-bit word."""
    M = [0x02,0x01,0x01,0x03]
    rot = M[(4 - col_idx) % 4 : ] + M[: (4 - col_idx) % 4]
    T = []
    for a in range(256):
        s = AES_SBOX[a]
        bytes_ = tuple(gmul(rot[i], s) for i in range(4))
        v = (bytes_[0] << 24) | (bytes_[1] << 16) | (bytes_[2] << 8) | bytes_[3]
        T.append(v)
    return T

T = [make_T(i) for i in range(4)]
print(f"T0[0]={T[0][0]:#010x}, T1[0]={T[1][0]:#010x}, T2[0]={T[2][0]:#010x}, T3[0]={T[3][0]:#010x}")

def find_ab(M, T_ref):
    """Find (a, b) such that M[i] = T_ref[i ^ a] ^ b, or None."""
    # Try each candidate a; b is then determined by M[0].
    for a in range(256):
        b = M[0] ^ T_ref[a]
        ok = True
        for i in range(256):
            if M[i] != T_ref[i ^ a] ^ b:
                ok = False; break
        if ok:
            return a, b
    return None

