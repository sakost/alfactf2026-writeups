def get(rid):
    e = roms[str(rid)]
    if isinstance(e, dict):
        max_idx = max(int(k) for k in e)
        arr = [0] * (max_idx + 1)
        for k, v in e.items():
            arr[int(k)] = v
        return arr
    return e

